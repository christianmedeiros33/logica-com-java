public class TimeAndDistance {
    public static void main(String[] args) {
        System.out.println("====== Distance traveled ======");
        double distanceTraveled = 30;
        double timeSpent = 0.25;
        double speedAverage = distanceTraveled / timeSpent;
        System.out.println("Speed average: " + speedAverage + "Km/h.");
    }
}
